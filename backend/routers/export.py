"""
Export Router — Generate PDF reports and Markdown exports.
GET /export/{comparison_id}/pdf
GET /export/{comparison_id}/markdown
GET /export/{comparison_id}/executive
"""
import logging
from datetime import datetime
from fastapi import APIRouter, HTTPException
from fastapi.responses import Response, PlainTextResponse

logger = logging.getLogger(__name__)
router = APIRouter()


